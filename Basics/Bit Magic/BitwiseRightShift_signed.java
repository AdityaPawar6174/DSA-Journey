public class BitwiseRightShift_signed {
    public static void main(String[] args) {
        int x = 33; // Binary: 00100001

        System.out.println(x >> 1); // Right shift by 1
        // The result will be 16 (Binary: 00010000)

        System.out.println(x >> 4); // Right shift by 4
        // The result will be 2 (Binary: 00000010)
    }
}
